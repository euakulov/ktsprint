package org.example.lesson_1

/*
Год полета – 1961
Взлет – 09:07
Посадка – 10:55*/

fun main() {
    val year = 1961
    var hour = "09"
    var minute = "07"

    println(year)
    println(hour)
    println(minute)

    hour = "10"
    minute = "55"

    print(hour)
    print(":")
    print(minute)
}
